package org.ashwini.basics;

//Beginning of HorseClass
public class Horse {
//Fields/Instance variables of HorseClass
	String color;
	String purpose;
	String breed;
	int height;
	int weight;
//Default constructor for HorseClass	
	Horse(){
		this.breed = "Arabian Quarter";
		this.color = "Black";
		this.height = 9;
		this.weight = 540;
		this.purpose = "Racing";
	}
	
	
}
